  void Page_1() throws Exception {
    CPResultSet Read_Cursor=null;
    try {
      /* Definizione variabili locali */
      /* pRapporto Char(25) */
      /* Verifica l'esistenza */
      /* _tipo Char(1) */
      /* Crea il comando da lanciare */
      // * --- Read from knarapbo
      m_cServer = m_Ctx.GetServer("knarapbo");
      m_cPhName = m_Ctx.GetPhName("knarapbo");
      m_cSql = "";
      m_cSql = m_cSql+"RAPPORTO="+CPSql.SQLValueAdapter(CPLib.ToSQL(pRapporto,"C",25,0,m_cServer),m_cServer,pRapporto);
      if (m_Ctx.IsSharedTemp("knarapbo")) {
        m_cSql = m_cSql+" and context_id='"+m_Ctx.GetTempID()+"'";
      }
      if (Read_Cursor!=null)
        Read_Cursor.Close();
      Read_Cursor = m_Sql.Query(m_cServer,"select "+CPLib.GetMultilanguageField("TIPORAP",m_Ctx,m_cServer)+" from "+m_cPhName+((m_cPhName).endsWith(CPSecurity.FILTER_QUERY_POSTFIX)?CPLib.NewCPCCCHK():"")+" where "+m_cSql);
      if ( ! (Read_Cursor.Eof())) {
        _tipo = Read_Cursor.GetString("TIPORAP");
        Read_Cursor.Next();
        if ( ! (Read_Cursor.Eof())) {
          Error l_oErrorFault = new Error("Read on knarapbo into routine arrt_lanciarap returns two or more records. This item should return only a record.");
          CPStdCounter.Error(l_oErrorFault);
        }
        Read_Cursor.Close();
      } else {
        // Error: no record found!
        _tipo = "";
        Read_Cursor.Close();
        m_cLastMsgError = "MSG_RECORD_NOT_FOUND";
      }
      /* Lancia la gestione */
      /* If _tipo = 'D' */
      if (CPLib.eqr(_tipo,"D")) {
        /* Return  */
        Forward f;
        f=new Forward("armt_anarap",false,this,Forward.View,false);
        f.SetParameter("RAPPORTO",pRapporto);
        f.SetParameter("m_cParameterSequence","RAPPORTO");
        f.SetParameter("m_cMode","hyperlink");
        throw f;
        /* ElseIf _tipo = 'P' */
      } else if (CPLib.eqr(_tipo,"P")) {
        /* Return  */
        Forward f;
        f=new Forward("armt_provanarap",false,this,Forward.View,false);
        f.SetParameter("RAPPORTO",pRapporto);
        f.SetParameter("m_cParameterSequence","RAPPORTO");
        f.SetParameter("m_cMode","hyperlink");
        throw f;
      } // End If
    } finally {
      try {
        if (Read_Cursor!=null)
          Read_Cursor.Close();
      } catch(Throwable dontCare) {
        // Ogni chiusura del cursore viene racchiusa in una catch per evitare di perdere l'eccezione
        // originata all'interno della try-finally della pagina della routine
      }
    }
  }
