  void Page_1() throws Exception {
    CPResultSet Cursor_intermbo=null;
    try {
      /* w_filename Char(100) */
      /* gPathDoc Char(128) // Path Documenti */
      /* gPathApp Char(128) // Path Documenti */
      /* gUrlApp Char(80) // URL Applicazione */
      /* gAzienda Char(10) // Azienda */
      /* _pwdsog Char(50) */
      /* _filedest Char(100) */
      /* _namedest Char(100) */
      /* _fileorig Char(128) */
      // * --- Select from intermbo
      m_cServer = m_Ctx.GetServer("intermbo");
      m_cPhName = m_Ctx.GetPhName("intermbo");
      if (Cursor_intermbo!=null)
        Cursor_intermbo.Close();
      Cursor_intermbo = m_Sql.Query(m_cServer,QueryLoader.SetSqlFunctions("select SIDPASS  from "+m_cPhName+((m_cPhName).endsWith(CPSecurity.FILTER_QUERY_POSTFIX)?CPLib.NewCPCCCHK():"")+" where "+(m_Ctx.IsSharedTemp("intermbo")?"context_id='"+m_Ctx.GetTempID()+"'":"1=1"),m_cServer,m_Ctx,true));
      while ( ! (Cursor_intermbo.Eof())) {
        /* _pwdsog := intermbo->SIDPASS */
        _pwdsog = Cursor_intermbo.GetString("SIDPASS");
        Cursor_intermbo.Next();
      }
      m_cConnectivityError = Cursor_intermbo.ErrorMessage();
      Cursor_intermbo.Close();
      // * --- End Select
      /* _fileorig := LRTrim(gPathDoc)+"/SID/INVII/STO/"+LRTrim(gAzienda)+"/"+LRTrim(w_filename) */
      _fileorig = CPLib.LRTrim(gPathDoc)+"/SID/INVII/STO/"+CPLib.LRTrim(gAzienda)+"/"+CPLib.LRTrim(w_filename);
      /* _namedest := Strtran(w_filename,".crypted","") */
      _namedest = CPLib.Strtran(w_filename,".crypted","");
      /* _filedest := LRTrim(gPathApp)+"/appo/"+LRTrim(gAzienda)+"/"+LRTrim(_namedest) */
      _filedest = CPLib.LRTrim(gPathApp)+"/appo/"+CPLib.LRTrim(gAzienda)+"/"+CPLib.LRTrim(_namedest);
      /* CriptoLibrary.AESdecryptFile(_fileorig,_filedest,_pwdsog) */
      CriptoLibrary.AESdecryptFile(_fileorig,_filedest,_pwdsog);
      /* Stop LRTrim(gUrlApp)+"servlet/DownloadFileDirect?fileName="+LRTrim(_filedest) */
      Forward f;
      f=new Forward(CPLib.LRTrim(gUrlApp)+"servlet/DownloadFileDirect?fileName="+CPLib.LRTrim(_filedest),false,this,Forward.NoStatus,true);
      f.SetParameter("m_cParameterSequence", "");
      f.SetParameter("m_cMode","hyperlink");
      throw f;
    } finally {
      try {
        if (Cursor_intermbo!=null)
          Cursor_intermbo.Close();
      } catch(Throwable dontCare) {
        // Ogni chiusura del cursore viene racchiusa in una catch per evitare di perdere l'eccezione
        // originata all'interno della try-finally della pagina della routine
      }
    }
  }
